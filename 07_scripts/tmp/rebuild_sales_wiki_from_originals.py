def clean_doc(text: str) -> str:
    return clean_inline_refs(strip_frontmatter(text))


def from_section(text: str, start: str, end: str | None = None) -> str:
    start_key = start if start in text else re.sub(r"^(#{2,3})\s+\d+\.\s+", r"\1 ", start)
    i = text.index(start_key)
    if end:
        end_key = end if end in text else re.sub(r"^(#{2,3})\s+\d+\.\s+", r"\1 ", end)
        j = text.index(end_key, i)
        return text[i:j].strip()
    return text[i:].strip()


def rewrite_title(text: str, title: str) -> str:
    if text.startswith("# "):
        text = re.sub(r"^# .+\n", f"# {title}\n", text, count=1)
    else:
        text = f"# {title}\n\n{text}"
    return text.strip() + "\n"


def intro(title: str, body: str, rest: str) -> str:
    return f"# {title}\n\n{body}\n\n{rest.strip()}\n"


def simplify_website_table(text: str) -> str:
    old_header = "| Страница | Назначение | Источник | Ревью |\n| --- | --- | --- | --- |"
    new_header = "| Страница | Назначение | Что проверить перед публикацией |\n| --- | --- | --- |"
    text = text.replace(old_header, new_header)
    new_lines = []
    for line in text.splitlines():
        if line.startswith("| Страница | Назначение |"):
            line = "| Страница | Назначение | Что проверить перед публикацией |"
        elif line.strip() == "| --- | --- | --- | --- |":
            line = "| --- | --- | --- |"
        if line.startswith("| ") and not line.startswith("| ---"):
            parts = [p.strip() for p in line.split("|")[1:-1]]
            if len(parts) == 4:
                line = f"| {parts[0]} | {parts[1]} | {parts[3]} |"
        new_lines.append(line)
    return "\n".join(new_lines)


