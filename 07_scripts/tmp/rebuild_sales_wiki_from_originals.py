def strip_frontmatter(text: str) -> str:
    text = re.sub(r"^# .+\n", "", text, count=1)
    text = re.sub(r"^Дата:.*\n?", "", text, flags=re.M)
    text = re.sub(r"^Статус:.*\n?", "", text, flags=re.M)
    text = re.sub(r"Источник:\n\n```text.*?```[\r\n]*", "", text, flags=re.S)
    text = re.sub(r"^Файл:\s*`[^`]+`\n?", "", text, flags=re.M)
    text = re.sub(r"```mermaid.*?```[\r\n]*", "", text, flags=re.S)
    return text.strip()


